CREATE TABLE CUSTOMERS
(CUSTOMER_ID DECIMAL (4,0) PRIMARY KEY,
FIRST_NAME CHAR(25),
LAST_NAME CHAR(25),
ADDRESS CHAR(60),
CITY CHAR(25),
STATE CHAR(2),
POSTAL_CODE DECIMAL(5,0), 
PHONE CHAR (14),
EMAIL CHAR(60) );
CREATE TABLE PRODUCTS
(PRODUCT_ID DECIMAL(4,0) PRIMARY KEY,
PRODUCT_NAME CHAR(60),
UNIT_PRICE DECIMAL(6,2),
IN_STOCK DECIMAL(3,0) );
CREATE TABLE ORDERS
(ORDER_NUM DECIMAL(4,0) PRIMARY KEY,
ORDER_DATE DATE,
SHIPPED_DATE DATE,
TRACKING_NUM CHAR(25), 
CUSTOMER_NUM DECIMAL (4,0),
ORDER_TOTAL DECIMAL (6,0) );
CREATE TABLE ORDER_DETAILS
(ORDER_NUM DECIMAL(4,0) REFERENCES ORDERS(ORDER_NUM),
PRODUCT_CODE DECIMAL (4,0),
QUANTITY DECIMAL (2,0),
PRICE_EACH DECIMAL (4,0),
PRICE_TOTAL DECIMAL (4,0) );
CREATE TABLE PAYMENTS
(PAYMENT_ID DECIMAL(4,0) PRIMARY KEY,
ORDER_NUM DECIMAL(4,0) REFERENCES ORDERS(ORDER_NUM),
CUSTOMER_NUM DECIMAL (4,0),
CARD_LAST4 DECIMAL (4,0),
EXP_DATE CHAR(7),
PAYMENT_DATE DATE,
PAYMENT_AMOUNT DECIMAL (4,0) );

